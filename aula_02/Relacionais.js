let numero1 = "1";
let numero2 = 1;
// == valor tem que ser igual
// faz conversão de valores para comparar
console.log(`Comparação igual: ${numero1 == numero2}`);

// === valor AND tipo tem que ser iguais
console.log(`Comparação estritamente igual: ${numero1 === numero2}`);

// !==  valor OR tipo são diferentes
console.log(`Comparação estritamente diferente: ${numero1 !== numero2}`);